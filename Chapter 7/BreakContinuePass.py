# Break statement

for i in range(100):
    if(i==34):
        break   # Exit the loop right now
    print(i)

# Continue statement

for i in range(100):
    if(i==34):
        continue     # Skip this iteration
    print(i)    


# Pass statement


for i in range(645):
    pass                # Without pass the program will through error


i = 0
while(i<45):
    print(i)
    i += 1